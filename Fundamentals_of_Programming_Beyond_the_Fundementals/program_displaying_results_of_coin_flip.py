# A Program displaying results of coin flips

flips = ['heads',
        'tails',
        'tails',
        'heads',
        'tails'
        ]

print(flips) # prints all the list

print(flips.count('heads')) # use count() method to return the number of elements that matches a certain string 'heads'

print(flips.pop()) # prints the last item in the list using pop() method
